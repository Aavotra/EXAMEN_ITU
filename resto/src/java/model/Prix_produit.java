package model;

import component.BdTable;

/**
 *
 * @author Admin
 */
public class Prix_produit  extends BdTable{
    int id;
    double montant;
    String date;

    public Prix_produit() {
    }

    public Prix_produit(double montant, String date) {
        this.montant = montant;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getMontant() {
        return montant;
    }

    public void setMontant(double montant) {
        this.montant = montant;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
}
